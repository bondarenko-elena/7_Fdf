#include "fdf.h"

void	init_map(t_map *map)
{
	map->dx = WIDTH * 0.5;
	map->dy = 300;
	map->sx = (WIDTH * 0.3) / (map->chars - 1);
	map->sy = (HEIGHT * 0.3) / (map->lines - 1);
	map->z = -2;
	map->color = 0xFF0000;
}
